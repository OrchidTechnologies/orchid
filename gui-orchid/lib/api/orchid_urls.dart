import 'package:orchid/api/orchid_crypto.dart';
import 'package:url_launcher/url_launcher.dart';

class OrchidUrls {
  static final preferredProviders = "https://www.orchid.com/preferredproviders";
  static final xdaiChain = "https://www.xdaichain.com/";
  static final contact = "https://orchid.com/contact";
  static final partsOfOrchidAccount =
      "https://docs.orchid.com/en/latest/accounts/#technical-parts-of-an-orchid-account";
  static final accountOrchid = 'https://account.orchid.com';
  static final join = 'https://www.orchid.com/join/';
  static final widget = 'https://account.orchid.com/widget/';
  static final contractV1EtherscanUrl =
      'https://etherscan.io/address/0x6dB8381b2B41b74E17F5D4eB82E8d5b04ddA0a82';
  static final contractV0EtherscanUrl =
      'https://etherscan.io/address/0xb02396f06CC894834b7934ecF8c8E5Ab5C1d12F1';
  static final contractV0GithubUrl =
      'https://github.com/OrchidTechnologies/orchid/blob/master/lot-ethereum/lottery0.sol';
  static final contractV1GithubUrl =
      'https://github.com/OrchidTechnologies/orchid/blob/master/lot-ethereum/lottery1.sol';

  static void openDapp({required EthereumAddress? signer}) {
    final uri = signer == null
        ? Uri.parse(OrchidUrls.accountOrchid)
        : Uri.parse(OrchidUrls.accountOrchid +
            '?signer=${signer.toString(prefix: true, elide: false)}');
    launchUrl(uri);
  }
}
